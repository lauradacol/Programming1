class Laboratorio{
	String nomeFantasia;
	String razaoSocial;
	String cnpj;
	String inscricaoEstadual;
	String endereco;
	String telefone;
	
	public Laboratorio(String nomeFantasia, String razaoSocial, String cnpj, String inscricaoEstadual, String endereco, String telefone){
		this.nomeFantasia = nomeFantasia;
		this.razaoSocial = razaoSocial;
		this.cnpj = cnpj;
		this.inscricaoEstadual = inscricaoEstadual;
		this.endereco = endereco;
		this.telefone = telefone;
	}

}

