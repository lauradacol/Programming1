    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
       int row = tabLoc.getSelectedRow();
       int columnFam = 0;
       int columnGen = 1; 
       int columnEsp = 2;
       
       Object u = tabLoc.getValueAt(row, columnFam);
       Object m = tabLoc.getValueAt(row, columnGen);
       Object l = tabLoc.getValueAt(row, columnEsp);

       String uf = u.toString();       
       String municipio = m.toString();       
       String localidade = l.toString(); 
       
       Localidade lRemove = LocalidadeFactory.getInstance().findLoc(uf, municipio, localidade);
       
       if((LocalidadeFactory.getInstance().deleteLoc(lRemove))==true){
            initLocTable();
       }
       
       else{
           ImpossivelDeletar frame = new ImpossivelDeletar();
           frame.setVisible(true);
       }
    }//GEN-LAST:event_btDeleteActionPerformed

    private void btUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btUpdateActionPerformed
       int row = tabLoc.getSelectedRow();
       int columnFam = 0;
       int columnGen = 1; 
       int columnEsp = 2;
       
       Object u = tabLoc.getValueAt(row, columnFam);
       Object m = tabLoc.getValueAt(row, columnGen);
       Object l = tabLoc.getValueAt(row, columnEsp);

       String uf = u.toString();       
       String municipio = m.toString();       
       String localidade = l.toString(); 

       Localidade l1 = LocalidadeFactory.getInstance().findLoc(uf,municipio,localidade);

       String ufNovo = addUFTextField.getText();
       String munNovo = addMunTextField.getText();
       String LocNovo = addLocTextField.getText();

       LocalidadeFactory.getInstance().updateLoc(l1, ufNovo, munNovo, LocNovo);
       initLocTable();
    }//GEN-LAST:event_btUpdateActionPerformed

