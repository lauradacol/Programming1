    private void btAddLocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddLocActionPerformed
        String UF = addUFTextField.getText();
        String municipio = addMunTextField.getText();
        String localidade = addLocTextField.getText();

        Localidade l = LocalidadeFactory.getInstance().cadastrarLocalidade(UF, municipio, localidade);

        initLocTable();
        addUFTextField.setText("");
        addMunTextField.setText("");
        addLocTextField.setText("");
    }//GEN-LAST:event_btAddLocActionPerformed

    private void initLocTable(){
        DefaultTableModel m = (DefaultTableModel) tabLoc.getModel();
        m.setRowCount(0);
        for(Localidade l : LocalidadeFactory.getInstance().findAll()){
            m.addRow(new Object[] {l.getUf(), l.getMunicipio(), l.getLocalidade()});
        }
    }       
    
